package com.rimidev.maxbook.entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author 1513733
 */
@Entity
@Table(name = "news", catalog = "bookstore_db", schema = "")
@NamedQueries({
    @NamedQuery(name = "News.findAll", query = "SELECT n FROM News n")
    , @NamedQuery(name = "News.findById", query = "SELECT n FROM News n WHERE n.id = :id")
    , @NamedQuery(name = "News.findByReaderLink", query = "SELECT n FROM News n WHERE n.readerLink = :readerLink")
    , @NamedQuery(name = "News.findByActiveStatus", query = "SELECT n FROM News n WHERE n.activeStatus = :activeStatus")})
public class News implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 255)
    @Column(name = "readerLink")
    private String readerLink;
    @Basic(optional = false)
    @NotNull
    @Column(name = "activeStatus")
    private boolean activeStatus;

    public News() {
    }

    public News(Integer id) {
        this.id = id;
    }

    public News(Integer id, String readerLink, boolean activeStatus) {
        this.id = id;
        this.readerLink = readerLink;
        this.activeStatus = activeStatus;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getReaderLink() {
        return readerLink;
    }

    public void setReaderLink(String readerLink) {
        this.readerLink = readerLink;
    }

    public boolean getActiveStatus() {
        return activeStatus;
    }

    public void setActiveStatus(boolean activeStatus) {
        this.activeStatus = activeStatus;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof News)) {
            return false;
        }
        News other = (News) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.rimidev.maxbook.entities.News[ id=" + id + " ]";
    }
    
}
